#include <iostream>
using namespace std;

int main() {
    bool trueValue, falseValue;
    int x=5, y=10;
    trueValue=x<y; // the variable trueValue is assigned the result of x<y
    falseValue=y==x; // the variable falseValue is assigned the result of y==x
    cout << "True is " << trueValue << endl;
    cout << "False is " << falseValue << endl;
}

